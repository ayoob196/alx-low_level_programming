#include "3-calc.h"

/**
 * op_add - c
 * @a: r
 * @b: r
 * Return: b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - s
 * @a: f
 * @b: r
 * Return: b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - s
 * @a: f
 * @b: r
 * Return: s
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a
 * @a: s
 * @b: a
 * Return: b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a
 * @a: a
 * @b: s
 * Return: a
 */
int op_mod(int a, int b)
{
	return (a % b);
}
